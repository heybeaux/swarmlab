import type { ClaimCase, CrossModelEvidence, ReceiptEvidence, RetrievalEvidence, SourceRelation } from './types.js';

const NOW = '2026-07-06T14:35:00.000Z';
const FUTURE = '2026-08-06T00:00:00.000Z';
const PAST = '2026-06-01T00:00:00.000Z';

function receipt(id: string, matches: boolean, signed = true, chainValid = true): ReceiptEvidence {
  return { id: `rcpt-${id}`, signed, chainValid, observationMatchesClaim: matches, actor: 'swarmlab-runner' };
}

function source(id: string, relation: SourceRelation, current = true): RetrievalEvidence {
  const expiresAt = current ? FUTURE : PAST;
  return {
    uri: `synthetic://ground-store/${id}`,
    relation,
    retrievedAt: NOW,
    expiresAt,
    digest: `sha256:${id}:${relation}:${expiresAt}`,
  };
}

function cm(supportVotes: number, contradictVotes: number, abstainVotes: number, correlatedError = false): CrossModelEvidence {
  return { supportVotes, contradictVotes, abstainVotes, correlatedError };
}

